# snake_case
# Start with lowercase or underscore
# Letters, numbers, underscores
# Case sensetive
# Don't overwrite keywords
# Starting with underscore _ signifies a private variable

user_name = "Ankur"
user_iq = 180

# Reassignment
user_age = user_iq / 4
my_age = user_age

print(user_name)
print(user_iq)
print(user_age)
print(my_age)

#  Multiple assignments
name, age, birth_year, is_programmer = "Ankur", 32, 1989, True
print(name, age, birth_year, is_programmer)
