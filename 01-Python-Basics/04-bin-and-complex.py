# Extra data type for numbers in python

# complex -> We do not use this most of the time
# Most commonly used are int and float

# Binary -> Returns the binary representation of a number

#  Convert Integer to binary
print(bin(5))  # 0b101

# Convert Binary to integer
print(int("0b101", 2))  # 5 -> using Base 2
print(int(0b101))  # 5
